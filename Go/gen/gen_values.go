//go:generate go run gen_values.go

package main

import (
	"fmt"
	"os"
)

func main() {
	genValuesForGame("gomoku", 5, 0, 1, 5, 25, 125, 1000, 1000, 1000)
	genValuesForGame("connect6", 6, 0, 1, 5, 25, 125, 625, 4000, 4000)
}

const header = `// Code generated by 'go generate game_of_stones/...'. DO NOT EDIT.

//go:build %s

package game

const (
	maxStones = %d
	maxStones1 = %d
	WinValue  = %d
)

`

func genValuesForGame(game string, maxStones int16, values ...int16) {
	file, err := os.Create("../game/value_tables_" + game + ".go")
	if err != nil {
		panic(err)
	}
	defer file.Close()

	fmt.Printf("Generating: game/%s_values.go\n", game)
	fmt.Fprintf(file, header, game, maxStones, maxStones-1, values[len(values)-1]/2)
	genValues(file, values)
	genDebugValues(file, values)
	genDebugValue(file, values)
}

func genValues(file *os.File, values []int16) {
	fmt.Fprintln(file, `var gameValues = [2][64][2]int16{`)
	fmt.Fprintln(file, "\t{ // turn = First")

	var vFirst [8][8][2]int16

	for i := range len(values) - 2 {
		vFirst[i][0] = [2]int16{-values[i], values[i+1] - values[i]}
		vFirst[0][i] = [2]int16{values[i] + values[i+2] - 2*values[i+1], values[i] - values[i+1]}
	}
	vFirst[0][0][1] = 0
	for y := range 8 {
		fmt.Fprint(file, "\t\t")
		for x := range 8 {
			fmt.Fprintf(file, "{%d, %d}, ", vFirst[y][x][0], vFirst[y][x][1])
		}
		fmt.Fprint(file, "\n")
	}
	fmt.Fprint(file, "\t},\n\t{ // turn = Second\n")
	var vSecond [8][8][2]int16

	for i := range len(values) - 2 {
		vSecond[0][i] = [2]int16{values[i] - values[i+1], values[i]}
		vSecond[i][0] = [2]int16{values[i+1] - values[i], 2*values[i+1] - values[i] - values[i+2]}
	}
	vSecond[0][0][0] = 0
	for y := range 8 {
		fmt.Fprint(file, "\t\t")
		for x := range 8 {
			fmt.Fprintf(file, "{%d, %d}, ", vSecond[y][x][0], vSecond[y][x][1])
		}
		fmt.Fprint(file, "\n")
	}
	fmt.Fprint(file, "\t},\n")
	fmt.Fprint(file, "}\n\n")
}

func genDebugValues(file *os.File, values []int16) {
	fmt.Fprintln(file, `var stoneValues = [64][2]int16{`)
	var v [8][8][2]int16

	for i := range values[1:] {
		v[0][i] = [2]int16{values[i+1] - values[i], -values[i]}
		v[i][0] = [2]int16{values[i], values[i] - values[i+1]}
	}
	v[0][0] = [2]int16{1, -1}
	printTable(file, v)
}

func genDebugValue(file *os.File, values []int16) {
	fmt.Fprintln(file, `var stoneValue = [64]int16{`)
	var v [8][8]int16

	for i := range values[1:] {
		v[0][i] = values[i]
		v[i][0] = -values[i]
	}
	for y := range 8 {
		fmt.Fprint(file, "\t")
		for x := range 8 {
			fmt.Fprintf(file, "%d, ", v[y][x])
		}
		fmt.Fprint(file, "\n")
	}
	fmt.Fprint(file, "}\n\n")
}

func printTable(file *os.File, v [8][8][2]int16) {
	for y := range 8 {
		fmt.Fprint(file, "\t")
		for x := range 8 {
			fmt.Fprintf(file, "{%d, %d}, ", v[y][x][0], v[y][x][1])
		}
		fmt.Fprint(file, "\n")
	}
	fmt.Fprint(file, "}\n\n")
}
